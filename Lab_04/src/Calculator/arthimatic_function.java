package Calculator;

public class arthimatic_function {
	
	// Addition Function
	float sum(float a, float b) {
		return (a + b);
	}
	
	// Subtraction Function
	float sub(float a, float b) {
		return (a - b);
	}
	
	// Multiply Function
	float mul(float a, float b) {
		return (a * b);
	}
	
	// Divide Function
	float div(float a, float b) {
		return (a / b);
	}
	
	// Module Function
	float mod(float a, float b) {
		return (a % b);
	}
}
